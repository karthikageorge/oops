package operators_Decisionmaking;

public class Vowel {
static String vowel="e";
	public static void main(String[] args) {
		
switch(vowel) {
case "a":System.out.println("vowel is "+vowel);
break;
case "e":System.out.println("vowel is "+vowel);
break;
case "i":System.out.println("vowel is "+vowel);
break;
case "o":System.out.println("vowel is "+vowel);
break;
case "u":System.out.println("vowel is "+vowel);
break;
default:System.out.println("INVALID");
}
	}

}
