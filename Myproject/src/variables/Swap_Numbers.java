package variables;

public class Swap_Numbers {

	public static void main(String[] args) {
	   int a=2,b=3,c;
	   System.out.println("a = " +a);
	   System.out.println("b= "+b);
	   c=a;
	   a=b;
	   b=c;
	   System.out.println(" after swapping value of a = "+a);
	   System.out.println(" after swapping value of b = "+b);

	}

}
